#ifndef __PNX8550_STANDBYCTL_H
#define __PNX8550_STANDBYCTL_H

#define PNX8550_STANDBYCTL_BASE 0xBBF04000
#define PNX8550_STANDBYCTL_MODE_OFFSET  0x00
#define PNX8550_STANDBYCTL_DATA_OFFSET  0x10

#define PNX8550_STANDBYCTL_MODE *(volatile unsigned long *)(PNX8550_STANDBYCTL_BASE + PNX8550_STANDBYCTL_MODE_OFFSET)
#define PNX8550_STANDBYCTL_DATA *(volatile unsigned long *)(PNX8550_STANDBYCTL_BASE + PNX8550_STANDBYCTL_DATA_OFFSET)
#define PNX8550_STANDBYCTL_MODE_MASK   0xfcffffff
#define PNX8550_STANDBYCTL_MODE_CONFIG 0x03000000
#define PNX8550_STANDBYCTL_ENABLE_POWEROFF  0x10000000
#define PNX8550_STANDBYCTL_POWEROFF_NOW  0x10001000

#endif
